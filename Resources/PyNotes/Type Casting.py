#type casting = convert the data type of a value to another data type.

x = 1 #int (integer = number without the decimal)
y = 2.0 #float (float = number with the decimal)
z = "3" #str (string = type the number multiple times)

x = float(x) #float can be change to int/str/other
y = float(y)
z = float(z)

print(x)
print(y)
print(z*3)