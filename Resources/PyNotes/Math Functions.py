import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #      ini untuk membulatkan si "pi" ke yang paling dekat
print(math.ceil(pi)) #  ini untuk membulatkan si "pi" ke atas
print(math.floor(pi)) # ini untuk membulatkan si "pi" ke bawah
print(abs(pi)) #        ini untuk membuat si "pi" menjadi absolut (non minus)
print(pow(pi,2)) #      ini untuk memangkatkan (pangkat) si "pi" 
                 #      NOTE: untuk angka stlh "," itu menandakan brp kali di pangkatkan (BISA DI GANTI)
print(math.sqrt(420)) # ini untuk mencari tau si "420" itu akar dari berapa, 
                 #      bisa juga digunakan untuk phytagoras
print(max(x,y,z)) #     ini untuk mencari tau diantara ketiga variable, manakah yang paling besar
print(min(x,y,z)) #     ini untuk mencari tau diantara ketiga variable, manakah yang paling kecil